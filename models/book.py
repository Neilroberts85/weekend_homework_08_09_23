class Book():
    def __init__(self, input_title, input_author, input_genre, input_checked_out):
        self.title = input_title
        self.author = input_author
        self.genre = input_genre
        self.checked_out = input_checked_out

def checked_out(self):
    self.checked_out = True

def checked_in(self):
    self.checked_out = False